export const getWardOptions = () => {
  return WARDS_DATA.map(ward => ({
    value: ward.wardNumber,
    label: `${ward.wardName} (${ward.wardNumber})`
  }));
};

export const getPollingUnitsForWard = (wardNumber: string) => {
  const ward = WARDS_DATA.find(w => w.wardNumber === wardNumber);
  if (!ward) return [];
  return ward.pollingUnits.map(pu => ({
    value: `${ward.wardNumber}-${pu.unitNumber}`,
    label: `PU ${pu.unitNumber}: ${pu.unitName}`
  }));
};
